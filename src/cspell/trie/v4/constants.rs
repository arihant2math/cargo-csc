// constants.rs
/// End of word
pub const EOW: char = '$';

/// Move up the tree
pub const BACK: char = '<';

/// End of Line (ignored)
pub const EOL: char = '\n';

/// Line Feed (ignored)
pub const LF: char = '\r';

/// Start of Absolute Reference
pub const REF: char = '#';

/// Start indexed of Reference
pub const REF_REL: char = '@';

/// End of Reference
pub const EOR: char = ';';

/// Escape the next character
pub const ESCAPE: char = '\\';
